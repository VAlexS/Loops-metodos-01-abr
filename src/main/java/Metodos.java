public class Metodos {

    // Method definition
    public static int addNumbers(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    public static void main(String[] args) {

        int result = addNumbers(5, 3);
        System.out.println("Sum: " + result); // Output: Sum: 8

    }
}
